import random

class LocalEngine:
    def __init__(self):
        self.templates = {
            "strategy": (
                "🌲 **Стратегический анализ от EL**\n\n"
                "Ниша: {topic}\n\n"
                "1. **АНАЛИЗ НИШИ**\n"
                "- SWOT: Сильные стороны в уникальности, слабости в масштабируемости.\n"
                "- Тренды 2024-2026: Автоматизация и персонализация.\n"
                "- Конкуренты: Локальные игроки и глобальные платформы.\n\n"
                "2. **БИЗНЕС-МОДЕЛЬ**\n"
                "- Монетизация: Модель подписки или разовые продажи.\n"
                "- Воронка: Лид-магнит -> Вебинар -> Продажа.\n\n"
                "3. **ПЛАН ЗАПУСКА (12 недель)**\n"
                "- Недели 1-4: Исследование и MVP.\n"
                "- Недели 5-8: Тестирование каналов трафика.\n"
                "- Недели 9-12: Масштабирование."
            ),
            "chat": [
                "Интересная мысль! Как солопренер, вы должны сосредоточиться на главном.",
                "Я рекомендую автоматизировать этот процесс, чтобы освободить время.",
                "Важно понимать юнит-экономику прежде чем масштабироваться.",
                "Давайте разберем это подробнее с точки зрения вашей стратегии."
            ]
        }

    def get_strategy_response(self, topic: str) -> str:
        return self.templates["strategy"].format(topic=topic)

    def get_chat_response(self) -> str:
        return random.choice(self.templates["chat"])

local_engine = LocalEngine()
